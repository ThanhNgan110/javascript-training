// numbers
let a = 100;
console.log(a);
console.log(typeof a);

// string
let firstName = "Jonas";
console.log(firstName);
console.log(typeof firstName);

// boolean
let fullAge = true;
console.log(fullAge);
console.log(typeof "fullAge");
fullAge = false;
console.log(fullAge);
console.log(typeof "fullAge");

// undefind
let name;
console.log(name);
console.log(typeof name);

// Object type
const person = { firstName: "Jonas", lastName: "John", age: "35" };
console.log(person);
console.log(typeof person);
